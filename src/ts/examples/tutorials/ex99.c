    mod->dim = 2;
    ierr = PetscOptionsInt("-dim","dimension of problem (2 or 3)","",mod->dim,&mod->dim,NULL);CHKERRQ(ierr);
    for (i=0;i<MAX_DIM;i++) N[i] = 1;
    for (i=0;i<mod->dim;i++) N[i] = 4; /* default size */
    ierr = PetscOptionsInt("-nx","number of cells in x direction","",N[0],&N[0],NULL);CHKERRQ(ierr);
    ierr = PetscOptionsInt("-ny","number of cells in y direction","",N[1],&N[1],NULL);CHKERRQ(ierr);
    ierr = PetscOptionsInt("-nz","number of cells in z direction","",N[2],&N[2],NULL);CHKERRQ(ierr);
    refinement_ratio = 2;
    ierr = PetscOptionsInt("-refinement_ratio","Multigrid refimenet ratio","",refinement_ratio,&refinement_ratio,NULL);CHKERRQ(ierr);
    user->vtkInterval = 1;
    ierr = PetscOptionsInt("-ufv_vtk_interval","VTK output interval (0 to disable)","",user->vtkInterval,&user->vtkInterval,NULL);CHKERRQ(ierr);
    overlap = 1;
    ierr = PetscOptionsInt("-ufv_mesh_overlap","Number of cells to overlap partitions","",overlap,&overlap,NULL);CHKERRQ(ierr);
