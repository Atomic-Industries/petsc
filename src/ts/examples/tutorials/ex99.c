/* U: (x^2-x^4)*(y^4-y^2)*(z^4-z^2) */
#undef __FUNCT__
#define __FUNCT__ "PhysicsSolution_Lap_x4_x2"
static PetscErrorCode PhysicsSolution_Lap_x4_x2(Model mod,PetscReal time,const PetscReal *x,PetscScalar *u,void *ctx)
{
  /* Physics        phys    = (Physics)ctx; */
  /* Physics_Lap *lap = (Physics_Lap*)phys->data; */
  PetscScalar r;
  PetscScalar e[MAX_DIM];
  PetscInt d;

  PetscFunctionBeginUser;
  for (d = 0; d < mod->dim; ++d) e[d] = x[d]*x[d];
  for (d = 0; d < mod->dim; ++d) e[d] *= 1 - e[d];
  for (r = 1.0, d = 0; d < mod->dim; ++d) r *= e[d];
  u[0] = r;
  PetscFunctionReturn(0);
}

/* RHS: Lap((x^2-x^4)*(y^4-y^2)*(z^4-z^2)) */
#undef __FUNCT__
#define __FUNCT__ "PhysicsSource_Lap_x4_x2"
static PetscErrorCode PhysicsSource_Lap_x4_x2(Model mod,PetscReal time,const PetscReal *x,PetscScalar *rhs,void *ctx)
{
  /* Physics        phys    = (Physics)ctx; */
  /* Physics_Lap *lap = (Physics_Lap*)phys->data; */
  PetscScalar r;
  PetscScalar x2[MAX_DIM]; /* = x*x; */
  PetscScalar a[MAX_DIM]; /* = x2*(1-x2) */
  PetscScalar b[MAX_DIM]; /* = 2-12*x2; */
  PetscInt d;

  PetscFunctionBeginUser;
  for (d = 0; d < mod->dim; ++d) x2[d] = x[d]*x[d];
  for (d = 0; d < mod->dim; ++d) a[d] = x2[d]*(1-x2[d]);
  for (d = 0; d < mod->dim; ++d) b[d] = 2-12*x2[d];

  /* cross products of coordinates prevents the use of D_TERM */
  if (mod->dim==2)
    r = b[0]*a[1] + a[0]*b[1];
  else if (mod->dim==3)
    r = b[0]*a[1]*a[2] + a[0]*b[1]*a[2] + a[0]*a[1]*b[2];
  else  if (mod->dim==1)
   r = b[0];
  else
    SETERRQ1(PETSC_COMM_SELF,PETSC_ERR_SUP,"No support for solution this DIM %d",mod->dim);
  rhs[0] = -r; /* keep solution positive */
  PetscFunctionReturn(0);
}
